-- دعم نوعين لإدخال المؤجل وقت فاتورة الشراء: نسبة % أو مبلغ ثابت للوحدة
-- (يُختار لكل بند على حدة في purchases.js)
--
-- اكتشاف مهم أثناء بناء هذا الملف: deferred_rebates.expected_amount
-- عمود generated ثابت بالصيغة (qty * rate) — يعني القاعدة من الأصل
-- بتفترض إن rate مبلغ فعلي للوحدة، مش نسبة %. purchases.js كان بيعرض
-- الحقل كنسبة % (0-100) ويحسب تقدير الفاتورة بصيغة lineTotal*rate/100،
-- لكن كان بيبعت الرقم الخام لعمود deferred_rate زي ما هو — يعني كان
-- فيه فرق بين الرقم المتوقع وقت كتابة الفاتورة والرقم الفعلي في تقرير
-- المؤجلات (deferred_rebates_supplier_summary) لأي صنف سعره مش 100
-- بالظبط. تأكدنا إنه مفيش بيانات حقيقية اتأثرت (كل المشتريات اللي فيها
-- مؤجل لحد دلوقتي تجريبية)، فمفيش داعي لأي backfill.
--
-- الحل: purchases.js دلوقتي بيحوّل أي % مدخلة لمبلغ فعلي للوحدة *قبل*
-- الحفظ (price × (1-disc/100) × pct/100)، فعمود deferred_rate بقى دايماً
-- مبلغ حقيقي للوحدة بصرف النظر عن النوع اللي اختاره المستخدم وقت
-- الإدخال — يعني qty*rate في القاعدة يفضل صحيح زي ما هو من غير أي
-- تعديل على الـ generated column أو الـ view. العمود الجديد هنا
-- (deferred_type) لغرض العرض فقط عند إعادة فتح فاتورة للتعديل، مش
-- مستخدم في أي حساب داخل القاعدة.
-- ════════════════════════════════════════════════════════════

ALTER TABLE purchase_items
    ADD COLUMN IF NOT EXISTS deferred_type text NOT NULL DEFAULT 'percent'
    CHECK (deferred_type IN ('percent', 'fixed'));
